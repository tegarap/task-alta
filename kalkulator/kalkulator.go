package kalkulator

func Additon(numberA, numberB int) int {
	result := numberA + numberB
	return result
}

func Substract(numberA, numberB int) int {
	result := numberA - numberB
	return result
}

func Mult(numberA, numberB int) int {
	result := numberA * numberB
	return result
}

func Div(numberA, numberB int) int {
	result := numberA / numberB
	return result
}
